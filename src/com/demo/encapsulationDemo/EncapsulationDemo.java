package com.demo.encapsulationDemo;

public class EncapsulationDemo {

	private int id;
	private String name;
	private String city;
	private long pnonNo;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public long getPnonNo() {
		return pnonNo;
	}
	public void setPnonNo(long pnonNo) {
		this.pnonNo = pnonNo;
	}
	
	

	
	
	

}
